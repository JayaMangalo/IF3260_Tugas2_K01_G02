    matrixOutput[2] = matrixInput[8];
    matrixOutput[3] = matrixInput[12];
    matrixOutput[4] = matrixInput[1];
    matrixOutput[5] = matrixInput[5];
    matrixOutput[6] = matrixInput[9];
